function longueurMot(mot) {
    return mot.length;
}

const mot1 = "HTML";
const mot2 = "CSS";
const mot3 = "JavaScript";
console.log(`La longueur du mot ${mot1} est de ${longueurMot(mot1)} lettres`);
console.log(`La longueur du mot ${mot2} est de ${longueurMot(mot2)} lettres`);
console.log(`La longueur du mot ${mot3} est de ${longueurMot(mot3)} lettres`);